package com.hemalatha.myimagesapp;

public class ImageModel {
    private String total,images,totalHints;

    public ImageModel(String images) {
        this.total = total;
        this.images = images;
        this.totalHints = totalHints;
    }

    public String getTotal() {
        return total;
    }

    public String getImages() {
        return images;
    }

    public String getTotalHints() {
        return totalHints;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public void setImages(String images) {
        this.images = images;
    }

    public void setTotalHints(String totalHints) {
        this.totalHints = totalHints;
    }
}
